// DAY 025 Increment & Decrement

public class AbdalBingung {
    public static void main(String[] args) {

        int a = 21;
        int b = 21;
        System.out.println("Nilai A SEBELUM Increment: " + a);
        System.out.println("Nilai B SEBELUM Decrement: " + b);

         a++; //Peintah INCREMENT, otomatis nilai dari variable a jdi bertambah 1. yg tadinya 21 berubah menjadi 22
         b--; //Perintah DECREMENT, otomatis nilai dari variable b jdi berkurang 1. yg tadinya 21 berubah menjadi 20

        System.out.println("Nilai A SETELAH Increment: " + a);
        System.out.println("Nilai B SETELAH Decrement" + b);
    }
}
