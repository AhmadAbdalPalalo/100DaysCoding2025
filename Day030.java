// DAY 030 Operator Perbandingan (<= dan >=)

public class AbdalUdahNyampeDayTigaPuluh {
  public static void main (String [] args) {
    int a = 21;
    int b = 12;
    System.out.println("Apakah A lebih KECIL/SAMA dngn B? " + (a <= b)); //Perbandingan "lebih kecil atau sama dengan"
    System.out.println("Apakah A lebih BESAR/SAMA dngn B? " + (a >= b)); //Perbandingan "lebih besar atau sama dengan"
  }
}
